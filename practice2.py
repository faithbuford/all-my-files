number1 = input("Type your first number: ")
number1 = number1
number2 = input("Type your second number: ")
number2 = number2 

addition = float(number1) + float(number2)
print(number1 + "+" + number2 + "=" + str(addition))

subtraction = float(number1) - float(number2)
print(number1 + "-" + number2 + "=" + str(subtraction))

multiply = float(number1) * float(number2)
print(number1 + "*" + number2 + "=" + str(multiply))

division = float(number1) / float(number2)
print(number1 + "/" + number2 + "=" + str(division))